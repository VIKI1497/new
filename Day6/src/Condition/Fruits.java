package Condition;

public class Fruits {
	public static void main(String[]args) {
		char a='Z';
		if (a=='A') {
			System.out.println("APPLE");
		}
		else if (a=='B') {
			System.out.println("BANANA");
		}
		else if (a=='C') {
			System.out.println("CUCUMBER");
		}
		else if (a=='D') {
			System.out.println("DARGON FRUIT");
		}
		else {
			System.out.println("NOT AVAIL");
		}
	}

}
