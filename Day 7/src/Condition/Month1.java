package Condition;

public class Month1 {
	public static void main(String[]args) {
	String month="AUGUST";
	switch(month) {
	case "JANUARY":
		System.out.println("FIRST MONTH");
		break;
	case "FEBUARY":
		System.out.println("SECOND MONTH");
		break;
	case "MARCH":
		System.out.println("THIRD MONTH");
		break;
	case "APRIL":
		System.out.println("FOURTH MONTH");
		break;
	case "MAY":
		System.out.println("FIFTH MONTH");
		break;
	case "JUNE":
		System.out.println("SIXTH MONTH");
		break;
	case "JULY":
		System.out.println("SEVENTH MONTH");
		break;
	case "AUGUST":
		System.out.println("EIGHT MONTH");
		break;
	case "SEPTEMBER":
		System.out.println("NINETH MONTH");
		break;
	case "OCTOBER":
		System.out.println("TENTH MONTH");
		break;
	case "NOVEMBER":
		System.out.println("ELEVENTH MONTH");
		break;
	case "DECEMBER":
		System.out.println("TWELETH MONTH");
		break;
	default:
		System.out.println("NOT VALID");
	
		
	}

	}
}


